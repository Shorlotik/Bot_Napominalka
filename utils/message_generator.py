"""Генератор автоматических сообщений на основе шаблонов"""
import random


# Шаблоны для генерации сообщений
TEMPLATES = [
    "любимая держи спинку прямо я тебя люблю",
    "дорогая не забывай про осанку я забочусь о тебе",
    "милая спину держи ровно я тебя обожаю",
    "любимка выпрями спинку я тебя очень люблю",
    "солнышко держи спину прямо я тобой дорожу",
    "дорогая следи за осанкой я тебя люблю",
    "любимая не сутулься я о тебе забочусь",
    "милая выпрями спинку я тебя обожаю",
    "дорогая держи спину ровно я тебя люблю",
    "любимка спинку ровно держи я тобой дорожу",
]

# Вариации начала
BEGINNINGS = [
    "любимая",
    "дорогая",
    "милая",
    "солнышко",
    "любимка",
    "дорогуша",
]

# Вариации напоминания
REMINDERS = [
    "держи спинку прямо",
    "не забывай про осанку",
    "спину держи ровно",
    "выпрями спинку",
    "не сутулься",
    "следи за осанкой",
    "спинку ровно держи",
]

# Вариации завершения
ENDINGS = [
    "я тебя люблю",
    "я тебя очень люблю",
    "я тебя обожаю",
    "я о тебе забочусь",
    "я тобой дорожу",
    "ты мне очень дорога",
]


def generate_message():
    """
    Сгенерировать новое сообщение на основе шаблонов
    """
    # С вероятностью 30% использовать готовый шаблон, иначе сгенерировать
    if random.random() < 0.3 and TEMPLATES:
        return random.choice(TEMPLATES)
    
    # Генерация нового сообщения из компонентов
    beginning = random.choice(BEGINNINGS)
    reminder = random.choice(REMINDERS)
    ending = random.choice(ENDINGS)
    
    message = f"{beginning} {reminder} {ending}"
    return message

